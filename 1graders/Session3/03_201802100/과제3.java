import java.util.Scanner;

public class 과제3 {

	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		System.out.print("갯수를 입력하시오:");
		int n = sc.nextInt();
		int y = 1 ;
		int i;
		while(n>=y) {
			
	
		for(i=1;y>=i;i++) {
			System.out.print("*");
		}
		y++;
		System.out.println();
		}
		
	}
		 }